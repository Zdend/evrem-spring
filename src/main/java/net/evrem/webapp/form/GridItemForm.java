package net.evrem.webapp.form;

public class GridItemForm {

	private Long gridItemId;

	private Integer w;
	private Integer h;
	private Integer x;
	private Integer y;

	public Integer getW() {
		return w;
	}

	public void setW(Integer w) {
		this.w = w;
	}

	public Integer getH() {
		return h;
	}

	public void setH(Integer h) {
		this.h = h;
	}

	public Integer getX() {
		return x;
	}

	public void setX(Integer x) {
		this.x = x;
	}

	public Integer getY() {
		return y;
	}

	public void setY(Integer y) {
		this.y = y;
	}

	public Long getGridItemId() {
		return gridItemId;
	}

	public void setGridItemId(Long gridItemId) {
		this.gridItemId = gridItemId;
	}

}
